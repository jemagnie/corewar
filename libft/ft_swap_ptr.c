#include "libft.h"

void	ft_swap_ptr(char **ptr1, char **ptr2)
{
	char	*tmp;

	tmp = *(char**)ptr1;
	*(char**)ptr1 = *(char**)ptr2;
	*(char**)ptr2 = tmp;
}
